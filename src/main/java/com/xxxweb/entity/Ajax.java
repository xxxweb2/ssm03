package com.xxxweb.entity;

public class Ajax {
    public int code;
    public String msg;

    @Override
    public String toString() {
        return "Ajax{" +
                "code=" + code +
                ", msg='" + msg + '\'' +
                '}';
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
